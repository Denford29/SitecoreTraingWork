namespace SATC.SC.Framework.SitecoreHelpers.Models
{
    /// <summary>
    /// public class to use for language models
    /// </summary>
    public class LanguageModel
    {
        /// <summary>
        /// get or set the language's native name
        /// </summary>
        public string NativeName { get; set; }

        /// <summary>
        /// get or set the language's url
        /// </summary>
        public string Url { get; set; }

        /// <summary>
        /// get or set the language's icon
        /// </summary>
        public string Icon { get; set; }

        /// <summary>
        /// get or set the language's two letter code
        /// </summary>
        public string TwoLetterCode { get; set; }

        /// <summary>
        /// get or set the language's name
        /// </summary>
        public string Name { get; set; }
    }
}
